let x = 2;
let y = 4;
let z = 6;

console.log("2 x 2 =", x * x)
console.log("2 x 4 =", x * y)
console.log("2 x 6 =", x * z)
console.log("6 x 4 =", z * y)
console.log("2 + 2 =", x + x)
console.log("2 + 4 =", x + y)
console.log("2 + 6 =", x + z)
console.log("6 + 4 =", z + y)
console.log("2 - 2 =", x - x)
console.log("2 - 4 =", x - y)
console.log("2 - 6 =", x - z)
console.log("6 - 4 =", z - y)